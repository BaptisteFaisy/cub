#include "cub.h"

int	check_input(char **tab)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (tab[i])
	{
		j = 0;
		while (tab[i][j])
		{
			if (tab[i][j] != '0' && tab[i][j] != '1' && tab[i][j] != 'N'
				&& tab[i][j] != 'S' && tab[i][j] != 'E' && tab[i][j] != 'W'
				&& tab[i][j] != ' ')
			{
				return (0);
			}
			j++;
		}
		i++;
	}
	return (1);
}
